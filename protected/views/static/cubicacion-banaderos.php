<h2>VACIADO Y LIMPIEZA.</h2>

<p>Vaciar en su totalidad el bañadero con bomba de vacío o autocebantes una vez realizada dicha tarea, limpiar el piso de la cal o los restos de pelo y barro que se encuentren, sacar todo con pala y llevarlo  lo más lejos de las instalaciones.
Recordar que el bañadero no se puede mantener durante muchos días vacío ya que se pueden rajar tanto el piso como las paredes, todas las actividades que debamos realizar para la limpieza y acondicionamiento del mismo deben ser rápidas (no demorar más de 72 hs) una vez vacío rasquetear con cepillo de acero todas las superficies utilizando todos los elementos de seguridad provistos, mascara y antiparras, mameluco, zapatos de seguridad y/o botas, guantes, etc.
</p>

<p>Cepilladas las paredes y el piso realizar la misma actividad en las instalaciones externas próximas, pileta de cubicación, escurridero, escalera, postes aledaños, techo etc., no deben quedar restos de formulaciones anteriores. Todos los elementos que se vayan a sumergir en el bañadero (agitadores, horquillas, reglas, baldes etc.) deberán estar limpios y exentos de restos de formulaciones anteriores.
Después de haber cepillado las instalaciones, utilizar la amoladora para sacar todo aquel material que no fue removido con el cepillo de alambre dentro del bañadero (piso y paredes)
Utilizar la amoladora con disco de desbaste, si el bañadero poseía una formulación de Amitraz anteriormente ASEGURARSE QUE NO QUEDEN RESTOS DE LA MISMA.
</p>

<p>Cuando se haya terminado de amolar se procederá a hidrolavar todas las superficies, comenzar siempre por las instalaciones externas y luego por el interior del bañadero, una vez terminada esta actividad, dejar secar unos minutos y observar si aparecen grietas en el piso o en las paredes del bañadero, en caso de que esto ocurra reparar las mismas para que no ocurran fugas del baño.</p>

<p>Terminada la limpieza del bañadero e instalaciones contiguas sacar  toda el agua utilizada con la hidrolavadora y dejar secar.</p>

<p>Siempre y cuando las instalaciones y el acceso al agua nos lo permita llenar con agua su totalidad dejarlo reposar y vaciarlo para un enjuague, posteriormente se lo llenara cubicándolo.</p>

<h2>LLENADO</h2>

<p>Previamente al llenado del bañadero  medir el pH del agua que vamos a colocar. El agua deberá poseer un pH que este entre 5,5 y 8 valores superiores o inferiores llevaran a la degradación de los principios activos.
Si es posible una vez cubicado el baño esperar unas 12 hs, volver a tomar el pH, si esta entre los parámetros pedidos, se podrá formular el baño.
</p>

<p>Para corregir  el pH en caso de que el agua sea demasiado alcalina debemos incorporar al agua del bañadero ácido clorhídrico [ICONO]/ ácido muriático e ir chequeando el descenso  del pH. Esto es fundamental en el momento de cargar el baño, sobretodo en campos con aguas duras. </p>

<h2>PREPARACION DEL BAÑO</h2>

<p>Seguir la siguiente metodologia</p>

<ul>
	<li>En un recipiente plástico de 500 litros y previamente graduado –recipiente “A”- se deberán cargar 500 litros de agua limpia.</li>

	<li>Se cargara el recipiente “A” con un balde previamente calibrado, hasta llegar a los 500 lt.</li>

	<li>Una vez colocado los 500 lt de agua en el recipiente “A” se realizara una marca en dicho recipiente, con una mecha para taladro se perforara el tanque en esta marca, de esta manera cuando se llegue a los 500 lt derramara por el agujero realizado con la mecha. El tanque deberá estar siempre sobre una superficie plana,  volcaremos el agua existente en el mismo al bañadero. De esta manera llenaremos el bañadero con 24 recipientes de 500 lt de agua. Ejemplo con Baño de 12.000 lt </li>

	<li>Finalizada la cubicación del Baño, tomaremos una muestra de agua para medir su pH.</li>

	<li>Finalizada la carga de agua, colocaremos el PRODUCTO ELEGIDO  (Aspersin-Cipersin-Garramix )  en el Bañadero de 12.000 lt</li>

	<li>Utilizaremos una probeta graduada para medir el volumen del PRODUCTO ELEGIDO a utilizar, dicho volumen será volcado en un recipiente”B”, hasta llegar al volumen deseado.</li>

	<li>Una vez enrasado el volumen total del PRODUCTO ELEGIDO en el recipiente “B”, se volcara en un balde y se prepara la emulsión con agua proveniente del bañadero recién cubicado, no agregar agua de otro lado, se mezclara con el pasaje de unos 30 animales y posteriormente tomaremos una muestra de la formulación preparada para ver la concentración del principio activo elegido. Dicha muestra se enviara al laboratorio de BIOGENESIS-BAGO S.A. planta de Monte Grande Pcia de Bs As.</li>
</ul>

<p>Cuando cubicamos el baño, rasar una regla cada 1000 Lt de agua. De ésta manera tendremos una forma de medir el consumo de la formulación del baño y podremos ajustar, la reposición y el refuerzo de acuerdo al consumo del baño, medido por la regla.</p>

<h2>Resumen de los pasos para el acondicionamiento del Bañadero</h2>

<ul>
	<li>Vaciado</li>
	<li>Limpieza en seco con cepillo de alambre.</li>
	<li>Limpieza en seco con amoladora.</li>
	<li>Retiro de todo el material seco con pala.</li>
	<li>Limpieza húmeda con Hidrolavadora.</li>
	<li>Vaciado y secado.</li>
	<li>Enjuagar con un llenado previo.</li>
</ul>


