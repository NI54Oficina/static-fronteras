<h2>Dosificación Aspersin inmersión</h2>

<h3> Pie de Baño</h3>
 <li>1 lt de Aspersin cada 1200 lt de agua</li>

<h3>Reposición</h3>
<li>1,5 lt de Aspersin cada 1200 lt de agua.</li>


<h3>Refuerzo en seco</h3>
<li>0,5 lt de Aspersin cada 1200 lt de agua</li>

<h3>Arrastre del producto</h3>
<li>De 1,5 y 2,5 lt por animal promedio, según la categoría y época del año que estemos bañando.</li>


<h2>Preparación del producto</h2>

<p>En un recipiente adecuado realizar una pre emulsión en un volumen de agua equivalente al doble de la cantidad del producto revolviendo hasta lograr una mezcla homogénea. Lo ideal es que se preparen 2,5 lt de Aspersin, en 5 lt de agua  las veces que sea necesario y añadir la pre mezcla a lo largo del baño, enjuagar el recipiente y volcar su contenido dentro del bañadero, deberá utilizarse siempre el agua ya cubicada para la preparación de la pre emulsión.</p>

<p>Agitar intensamente durante 10 minutos el baño, luego hacer pasar entre 30 a 50 animales para homogeneizar el baño, estos animales serán bañados nuevamente al final del pasaje de la tropa.</p>

<p>Limpieza de Tropas: Dos baños con intervalos de 9 días.</p>

<p>Para control de Parasitación: Bañar cada 21 días.</p>


<h2>Período de retiro</h2>

<p>No sacrificar animales para consumo humano hasta haber transcurrido 17 días del último tratamiento.</p>


