<h2>Dosificación GARRAMIX</h2>

<h3>Pie de Baño</h3>
<li>4 lt de Garramix cada 2000 lt de agua.</li>
<li>20 Kg de Estabilizante cada 2000 lt de agua</li>

<h3>Reposición</h3>
<li>4 lt de Garramix cada 1000 lt de agua.</li>
<li>20 Kg de Estabilizante cada 1000 lt de agua</li>

<h3>Refuerzo en seco</h3>
<li>4 lt de Garramix por cada 2.000 lt que disminuyó el nivel del Bañadero.</li>

<h3>Arrastre del Producto</h3>

<p>3 a 4 lt por animal, según la categoría y época del año que estemos bañando.</p>
<p>Limpieza de Tropas: Dos baños con intervalos de 9 días</p>

<p>Para control de Parasitación: Bañar cada 21 días</p>

<h2>Periodo de retiro</h2>

<p>Entre el último tratamiento y la faena para consumo deben transcurrir 14 días.</p>

<p>Entre el último tratamiento y el ordeñe para consumo humano y/o industrialización de la leche deben transcurrir 48 horas.</p>


