	<h2>Bienestar Animal</h2>

	<p>Existen muchas definiciones de Bienestar Animal (BA), sin embargo, todas coinciden en asegurar
	que implica no sólo el estado físico en que se encuentran los animales sino también el estado
	mental, pudiendo expresar su comportamiento natural sin sufrir dolor, miedo o estrés.</p>

	<p>El dolor y el miedo producen estrés, con consecuencias en el organismo animal, entre las que
	podemos contar: aumento de la frecuencia cardíaca, aumento de la concentración de adrenalina,
	menor consumo de alimento, menor producción de leche o carne, así como también alteraciones en
	la reproducción e inmunidad. En definitiva, se afecta la eficiencia de producción de los rodeos.</p>

	<p>Existen medidas sencillas de manejo que reducen el estrés en los animales con los que estamos trabajando:</p>

	<ul>
		<li>Durante los traslados, recordar que las tropas o rodeos mantienen una escala social para la marcha y es importante respetarla, al igual que la velocidad de marcha.</li>
		<li>No utilizar palos, picanas ni perros durante el trabajo en la manga. Esto sólo causa intranquilidad en los animales y en la mayoría de los casos también implica mayor pérdida de tiempo.</li>
		<li>Utilizar banderas blancas para el arreo y el trabajo en el toril, siempre abordando los animales desde atrás.</li>
		<li>Personal entrenado y capacitado en buenas prácticas de manejo y bienestar.</li>
		<li>No utilizar caballos dentro de los corrales de la manga ni el toril.</li>
	</ul>




	<h2>Toma de datos</h2>

	<p>Partamos de la premisa de que, todo lo que no se anota se olvida o se pone en cuestionamiento. Por
	tal motivo, se debe llevar una planilla donde anotar datos como: N° de tropa, cantidad de animales
	tratados, fecha de tratamiento, producto aplicado, serie y fecha de vencimiento del producto, dosis
	aplicada, fecha de retratamiento y observaciones.</p>

	<p>Poseer estos datos nos permitirá tomar decisiones a futuro como retratamientos, corrección del plan sanitario, etc.</p>


	<h1 class="col-lg-12 col-md-12 col-sm-12 col-xs-12 title-producir-inside color-manejo">Aplicación de los productos biológicos</h1>

	<p>Un mundo que crece de forma constante demandando más y mejores alimentos, exige un compromiso con la eficiencia productiva que optimice el uso de los recursos.</p>

	<p><strong>Los proveedores de insumos para la salud animal deben y deberán ofrecer al productor y al veterinario las mejores de cada animal.</strong></p>
	<p>La prevención es el objetivo de un plan sanitario aplicado en tiempo y forma, garantizando un rodeo sano y más eficiente.</p>
	<p>Para lograr una adecuada homogeneidad de la vacuna, el frasco debe ser agitado al retirarlo de la heladera del campo y nuevamente en la manga antes de cargar la jeringa.</p>
	<p>En el caso de algunas vacunas, como las clostridiales, esa agitación debe ser más enérgica y por
	más tiempo ya que el sedimento que se forma en el fondo del frasco es más difícil de resuspender
	que en otra vacuna.</p>
	<p>Esta homogeneización del producto es fundamental para asegurar una correcta proporción entre
	vehículo y antígeno en todas las dosis a aplicar.</p>

	<h2>Cadena de Frío</h2>

	<p>Los productos biológicos (vacunas) requieren una temperatura constante para su transporte,
	conservación y manejo en la manga. Si por algún motivo se pierde la cadena de frío, el poder
	antigénico se pierde y con ello la eficacia.</p>

	<p>Las temperaturas adecuadas son:</p>

	<p>Transporte y Conservación: 2 °C a 8 °C</p>

	<p><strong>Importante:</strong>realizar un monitoreo periódico de la temperatura de la heladera en la cual se
	conservan las vacunas.</p>

	<p>Mantener un orden dentro de la heladera, almacenando los productos de manera tal que se utilicen
	primero aquellos que tengan un vencimiento más próximo y colocar en lugares de mayor
	accesibilidad los productos de uso más frecuente. De esta manera se evita mantener la puerta de la
	heladera abierta por tiempo innecesario.</p>

	<p>Durante el trabajo en la manga: utilizar dos conservadoras con refrigerante suficiente para mantener
	la temperatura de conservación dentro de los parámetros esperados (2 °C a 8 °C). En la primera,
	mantener los envases con aquellas dosis que aún no serán necesarias, mientras que en una más
	pequeña y accesible, colocar el envase en uso al igual que las jeringas cuando las mismás no estén
	siendo utilizadas. De esta manera no sólo se evita la exposición al sol sino también las variaciones
	de temperatura que determinan la pérdida de eficacia del producto.</p>

