	<h1 class="col-lg-12 col-md-12 col-sm-12 col-xs-12 title-producir-inside color-manejo">Premisas para un Trabajo Bien Hecho</h1>

	<div class="col-lg-9 col-md-9 col-sm-9 col-xs-12 producir-inside-container" >


	<p>Hacer rápido un trabajo no es sinónimo de calidad. Hay que tener en cuenta que el trabajo en la manga implica costos no sólo de los productos a utilizar, sino también, y quizás lo más importante, implica un costo del personal y de trabajar sobre los animales.
	</p>

	<p>Es por eso que, para cualquier trabajo de manga, es necesario realizar un relevamiento de diferentes factores que intervienen en el mismo: personal asignado, caballos, herramientas, instalaciones,cantidad de animales a tratar, categorías de hacienda, productos a aplicar, potreros donde arrimar las tropas, agua y sombra para la post aplicación, estado de los animales etc. El hecho de mantener estas variables controladas, garantizará un trabajo ordenado y eficiente.
	</p>


	<h2>Encierre y aparte</h2>

	<p>El encierre de los animales debe realizarse al menos 2 horas antes del trabajo de manga. En caso que el trabajo se lleve a cabo en horario de la mañana, realizar el encierre el día anterior, quedando
	los animales en una plazoleta con acceso al agua. En épocas de verano, se recomienda evitar trabajar en horas de intenso calor y contar con sombra adecuada para los animales en espera.
	</p>

	<p>Previo al ingreso al toril, debe hacerse el aparte de los animales por categoría, estableciendo un orden prioritario de ingreso a la manga según estado fisiológico (hembras preñadas), tamaño (separar vacas de terneros para evitar posibles golpes y lesiones), animales astados, etc.
	</p>

	<h2>Instalaciones y Ambiente</h2>

	<p>Es de suma importancia que el lugar de trabajo sea seguro, tanto para los operarios como para los
	animales. En la manga y corrales, no debe haber tablas o varillas quebradas, tornillos sobresalientes
	o cualquier otro elemento que pueda producir lesiones, causa necesaria para el desarrollo de
	infecciones como por ejemplo Mancha.</p>

	<p>Existen cepos que por su estructura, al cerrarse, causan lesiones a nivel de la paleta (lesión del
	nervio radial a nivel de la escápula); en estos casos, es conveniente colocar una protección sobre el
	cepo que amortigüe el golpe o bien reemplazarlo. Este tipo de lesión también puede presentarse
	cuando animales de baja condición corporal (animales flacos) atraviesan la manga corriendo y son
	encepados. Es por este motivo que el trabajo pausado es una medida preventiva.</p>

	<p>Es importante el estado de los pisos sobre los que estarán parados los animales, ya sea en el corral
	de espera, el toril, la manga o los corrales de post tratamiento. Esto evitará lesiones
	fundamentalmente en el sistema locomotor.</p>

	<p>Presencia de piedras, barro excesivo, charcos de agua, etc predisponen a este tipo de lesiones y
	deben evitarse.</p>

	<h1 class="col-lg-12 col-md-12 col-sm-12 col-xs-12 title-producir-inside color-manejo">Medidas de Seguridad y Manejo</h1>

	<h2>De los Operarios</h2>

	<p>Antes de comenzar a trabajar, las tareas deberán ser distribuidas claramente, respetando la cadena
	de mando dentro del personal y teniendo en cuenta las aptitudes individuales, en pos de un trabajo
	ordenado, eficiente y bajo un ambiente cordial. También es conveniente la rotación del personal en
	las tareas de manga para evitar la monotonía del trabajo y conseguir de ésta manera que el personal
	esté más atento.</p>


	<p>Se debe contar con los elementos de trabajo y protección personal adecuados. Por ejemplo, en el
	caso de productos con fosforados o piretroides, se recomienda el uso de guantes. También un
	botiquín de Primeros Auxilios para la atención de cualquier eventualidad o accidente que pudiera
	llegar a ocurrir.</p>


	<h2>De los Animales</h2>

	<p>Al momento de la vacunación, todos los animales deben encontrarse clínicamente sanos. Para ello,
	es necesario realizar la detección y diagnóstico adecuados de animales enfermos, con altas cargas
	parasitarias, etc, y realizar los tratamientos necesarios al menos dos semanas antes de la
	administración de las vacunas. De esta manera, se consigue una respuesta óptima del sistema
	inmune a la dosis vacunal.</p>


	<h2>Algunos puntos a tener en cuenta:</h2>
	<ul>
		<li>No realizar aplicaciones con el cepo abierto, ya que se pierde todo tipo de control en cuanto a la
		dosis y el sitio de aplicación del producto, pudiendo producirse lesiones o abscesos en dicho sitio.<br/>En muchas oportunidades también se golpea un hueso con la punta de la aguja, generando una
		ruptura en la misma que a su vez, causa lesiones en los posteriores animales en que se utilice.</li>

		<li>No vacunar animales mojados ni extremadamente sucios, ya que esto predispone la producción de abscesos por infección en el punto de inoculación.</li>

	</ul>

	<p>Llevar un conteo de los animales vacunados, para luego cotejar el número con la cantidad de dosis administradas según los envases descartados.</p>

	<p>Al aplicar cualquier tipo de producto, siempre existe el riesgo potencial de generar reacciones
	adversas como Shock Anafiláctico. Por este motivo se recomienda, en todo momento, observar los
	animales ya tratados y contar siempre con Adrenalina o Dexametasona para tratar los animales en
	caso de Anafilaxia.</p>


	<h2>Higiene</h2>

	<p>Se recomienda que todo el material utilizado en la manga para la aplicación de productos sea
	lavado, desinfectado y acondicionado para el próximo trabajo.</p>

	<h2>Material Descartable y Reutilizable</h2>

	<p>Todos los materiales, aunque sean reutilizables, tienen una vida útil. Por ejemplo, las agujas deben
	reemplazarse cada 100 animales; al perder el filo, la aguja comienza a ejercer una acción de
	sacabocado, este pequeño trozo de piel, que es inyectando junto con el producto que estamos
	aplicando, está contaminado con millones de bacterias que generarán absceso en el sitio de
	aplicación.</p>

	<p>Debe elegirse el tamaño de aguja adecuado de acuerdo al producto que vamos a utilizar, la vía
	empleada y el tamaño del animal a tratar. A continuación, un cuadro representativo.</p>


