	<p>Utilizar preferentemente jeringas automáticas, correctamente calibradas para administrar la dosis
	indicada. Controlar el volumen de descarga y rechequear que el mismo no varíe a lo largo de las
	aplicaciones. No se recomienda utilizar autodosificadores ya que en éstos se pierde la cadena de
	frío. Puede administrarse más de una vacuna al mismo tiempo, pero debe tenerse la precaución de
	hacerlo siempre en sitios de aplicación diferentes con jeringas diferentes. Nunca mezclar en una
	misma jeringa dos productos ni utilizar una sola jeringa para dos productos diferentes sin la correcta
	limpieza previa. Por ejemplo, si se administra una vacuna para la prevención de clostridiales junto a
	una vacuna reproductiva, aplicar la primera siempre en el flanco derecho y la segunda en el flanco
	izquierdo del animal. De esta manera, se puede realizar el seguimiento de la inoculación en caso de
	ocurrencia de abscesos, reacciones anafilácticas, etc.</p>

	<p>Es importante también llevar un registro de las vacunas administradas, con número de lote del producto y dosis.</p>





