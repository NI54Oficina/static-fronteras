<p>El aborto es definido como la interrupción de la gestación y la consecuente pérdida del

feto. Existen diversas causas de aborto con diferentes mecanismos de acción:

toxicológicas, endocrinológicas, metabólicas, infecciosas, etc. Los bovinos son susceptibles

a los agentes infecciosos sobre todo en el primer y segundo tercio de la gestación. Pueden

afectar al embrión (antes de los 42 días) o feto (entre 42 y 260 días) en cualquier etapa de

su desarrollo, ocasionando la muerte (con o sin expulsión), malformaciones congénitas,

natimortos, nacimiento de crías débiles o nacimiento de crías persistentemente

infectadas.</p>


<h2>Principales enfermedades bacterianas asociadas al aborto bovino infeccioso</h2>
<h3>a. Campylobacteriosis genital:</h3>

<p>Campylobacter fetus tiene gran difusión y causa infertilidad debido a muerte embrionaria

temprana y ocasionalmente aborto. Luego de una infección inicial las bacterias se

eliminan del útero pero pueden persistir por períodos prolongados en la mucosa vaginal.

Esta enfermedad puede también ser transmitida por inseminación artificial a través del

semen, que no haya sido analizado, o material contaminado (iatrogénico).

C. fetus sp. fetus se encuentra en el intestino del bovino y causa abortos esporádicos. La

infección con Campylobacter fetus sp. venerealis usualmente causa infertilidad temporaria

o muerte tardía del embrión o mortalidad temprana del feto (30‐70 días de gestación),

aunque también causa abortos esporádicos entre los 4 y 8 meses de gestación.

La Campylobacteriosis es caracterizada clínicamente por repetición de celos, ciclos

irregulares en las hembras, y prolongación del período de servicio. La fertilidad

usualmente retorna a los 4‐8 meses luego de la infección inicial.

Muestras de esmegma prepucial, semen, fluido fetal, placenta y descargas vaginales

pueden contribuir al diagnóstico.

La forma de prevenir la enfermedad es a través de la vacunación de machos y hembras y

del control sanitario de todos los toros a través del raspaje prepucial.</p>

<h3>b. Brucelosis:</h3>

<p>Es una enfermedad zoonótica que usualmente produce abortos. La presencia de metritis y

retención de placenta son secuelas comunes del aborto.

La enfermedad es crónica por naturaleza. Luego de multiplicarse B. abortus se ubica

principalmente glándula mamaria, nódulos linfáticos mamarios y útero grávido. La

infección uterina ocurre durante el segundo mes de la gestación. El aborto ocurre

usualmente luego del 5º mes de gestación.

B. abortus puede ser aislada de fluido abomasal fetal, pulmón, placenta, fluido uterino y

leche.

La vacunación de las terneras, antes de los 8 meses de vida, es la herramienta más

efectiva para la prevención de ésta enfermedad.</p>


<h3>c. Leptospirosis:</h3>

<p>
  Es una enfermedad zoonótica (se transmite de los animales al hombre) de distribución

mundial. En Argentina, el serovar más importante en brotes de Leptospirosis bovina es L.

pomona, pero también existen aislamientos de los serovares canicola e

icterohaemorrhagiae. El serovar hardjo, importante en otras regiones, probablemente

esté presente en nuestro medio, pero aún no ha sido aislado de casos clínicos.

La bacteria es eliminada por orina de animales portadores que pueden ser silvestres,

como los roedores, o domésticos, como el cerdo. Los animales (o el hombre) se infectan

por contacto directo con la misma, o indirecto a través de agua o alimentos

contaminados. La presencia de agua y barro favorece la persistencia de Leptospira en el

medio ambiente.

Signos de Leptospirosis aguda, como fiebre, anemia hemolítica, hemoglobinuria, ictericia y

alta mortalidad suelen observarse en animales jóvenes. En vacas en lactancia, puede

producir agalactia y mastitis.

Produce abortos en forma de “tormenta” (muchos casos en un corto período de tiempo) y

muerte de animales jóvenes.

Las muestras a obtener para el diagnóstico son: orina, suero fetal y de la madre, placenta

y tejidos fetales. El aislamiento bacteriano es poco eficaz como rutina de diagnóstico.

La vacunación de los rodeos es la herramienta más efectiva para la prevención de ésta

enfermedad.
</p>

<h3>d. Histophilus somni (ex Haemophilus somni):</h3>
<p>En el bovino es asociado con poliartritis, enfermedad respiratoria e infecciones

reproductivas, abortos esporádicos y nacimiento de terneros débiles. Se ha evidenciado

una alta prevalencia de H. somni en el tracto urogenital de los bovinos siendo esta la

fuente de infección más significativa, ya que puede ser inhalado, ingerido o transmitido en

forma venérea.

H. somni ha sido encontrado en el tracto urinario y prepucio de toros, esto sugiere que el

organismo es transmitido a la vaca por el toro durante el servicio y es eliminado a través

de descargas uterinas/vaginales y orina de animales infectados. El tracto reproductivo es

reservorio de la enfermedad, pudiendo ocasionar la muerte del feto, a cualquier edad, con

subsiguiente aborto e infección uterina.

Las muestras para el diagnóstico incluyen fluido fetal, placenta y descargas vaginales. Para

el diagnóstico se debe considerar el conjunto de información: cultivo en pureza del

contenido estomacal o tejidos; presencia de lesiones inflamatorias; descarte de otra causa

aborto.

La vacunación de los rodeos es la herramienta más efectiva para la prevención de ésta

enfermedad.</p>


<h3>e. Abortos esporádicos asociados a bacterias oportunistas:</h3>

<p>
  Existen diversas bacterias oportunistas asociadas con infecciones de la placenta y el feto,

pudiendo ocasionar aborto. Estás bacterias normalmente se encuentran en el ambiente o

en las superficies mucosas. Entre ellas se pueden mencionar: Arcanobacterium pyogenes

(ex Actinomyces pyogenes), Bacillus spp., Escherichia coli, Pasteurella spp., Pseudomonas

spp., Serratia marcescens, Staphylococcus spp., Streptococcus spp., aunque otras bacterias

también han sido identificadas e incluidas en el grupo de oportunistas esporádicas

causales de aborto.

El aborto puede ocurrir en cualquier estadio de la gestación, pero la mayoría de las veces

se produce en la segunda mitad. La placenta puede o no estar retenida y la madre no

presenta signos específicos.
</p>
<h3>f. Otros agentes patógenos que ocasionan pérdidas reproductivas:</h3>
<p>
  Otros agentes bacterianos pueden ocasionar abortos, como Listeria monocytogenes,

Chlamydia sp., Ureaplasma diversum, Salmonella sp., E. coli, etc.
</p>
<h2>2. Principales protozoarios asociadas al aborto bovino infeccioso:</h2>

<h3>a. Neosporosis bovina:</h3>

<p>
  Enfermedad parasitaria que afecta a caninos, bovinos, ovinos, caprinos y ciervos causada

por el protozoo Neospora caninum. Es una infección común del ganado bovino y es la

mayor causa de aborto en todo el mundo, siendo de las más importantes en ganado

lechero.

En vacas adultas ocasiona abortos entre el 3º mes hasta el final de la gestación, aunque

más frecuentemente ocurre entre el 4º y 8º mes. Aunque no es exclusivo, la momificación

fetal es un hallazgo. Además del aborto pueden nacer terneros con lesiones cerebrales,

con manifestaciones nerviosas y débiles. La infección con N. caninum durante la gestación

genera terneros clínicamente normales. También pueden nacer terneros de apariencia

normal pero infectados silentes, lo que resultará (en el caso de una hembra) en la

infección a su descendencia cuando se preñe, manteniendo la infección en el rodeo. Este

mecanismo, denominado vertical, es la principal forma de infección en el bovino. La otra

vía de infección se define como horizontal y se da a través de la ingestión de ooquistes de

las deyecciones de lo huésped definitivo, el perro.

El examen patológico del feto abortado y la serología se pueden utilizar para confirmar la

infección con Neospora. El diagnóstico patológico se basa en la presencia de lesiones

típicas, identificación del protozoo por inmunohistoquímica, PCR y test microbiológicos y

serológicos para descartar otras causas.
</p>
<h3>b. Tricomonosis bovina:</h3>
<p>
  Causada por Tritrichomona foetus es una enfermedad venérea caracterizada por

subfertilidad y pérdida de la preñez temprana. La prevalencia de la enfermedad en toros

aumenta con la edad debido a una profundización de las criptas epiteliales en el prepucio.

La transmisión es principalmente a través del servicio (cópula). El toro es el portador

crónico y asintomático.

Los abortos ocurren generalmente en los primeros cinco meses de gestación. Es menos

común el aborto en la mitad y último término de la gestación por T. foetus. Los fetos

abortados pueden ser autolíticos y sin lesiones y la placenta puede estar edematosa.

La identificación de T. foetus se hace a partir de fluidos por observación directa o por

cultivo para confirmar el diagnóstico. Los parásitos son obtenidos e identificados más

eficientemente a partir de esmegma prepucial de toros infectados. También pueden estar

presentes en la placenta o líquido de abomaso.
</p>

<h2>3. Principales enfermedades virales asociadas al aborto bovino infeccioso:</h2>

<h3>a. Virus de la Diarrea Viral Bovina (DVB):</h3>
<p>
  Es un Pestivirus y se describen diferentes cepas, como los serotipos 1a, 1b y 2. Pueden ser

citopáticas (CP) o no citopáticas (NCP). Este nombre no tiene que ver con la patogenicidad

en los animales sino como una forma de comportamiento en cultivos celulares.

Se puede transmitir por contacto directo y vía transplacentaria, infectando al feto

pudiendo producir muerte embrionaria o fetal. Los fetos infectados antes del desarrollo

del sistema inmunitario pueden generar nacimiento de terneros persistentemente

infectados (PI). Normalmente el 50% muere antes de los 2 años. No obstante esto, es la

principal fuente de virus en los rodeos ya que una escasa proporción sobrevive actuando

como un importante reservorio de la infección.

A un rodeo puede ingresar de diferentes formas, como por ejemplo al introducir animales

infectados en cualquier estadio fisiológico, en forma iatrogénica por el uso de semen

infectado o a través de agujasen la aplicación de productos inyectables. También el uso de

vacunas vivas en vacas preñadas puede ser una fuente de infección.

Podemos separar a la acción del virus en dos etapas bien definidas:

1. Etapa de gestación, se produce una infección fetal con diferentes anormalidades

dependiendo si es un virus CP (puede producir abortos en cualquier momento de

la gestación) o NCP. En el caso de ser NCP, el virus es capaz de atravesar la barrera

placentaria, invadir el feto y originar diferentes anormalidades. Dependiendo del

momento de la gestación en la que se produzca la infección podemos observar

distintas manifestaciones:

</p>
<p>
  <li>a. Si se produce hasta los 120 días de gestación puede provocar aborto o

  nacimiento de terneros PI.</li>

  <li>b. Si esta se produce entre los 120 y 180 días de gestación puede ocasionar

  aborto o malformaciones congénitas.
  </li>
  <li>c. Si la infección sucede después de los 180 días puede provocar de igual

  forma aborto aunque el feto es capaz de generar sus propios anticuerpos,

  nace positivo serológicamente pero no está infectado.</li>

</p>

<p>
  2. En etapa post parto. Se pueden describir dos enfermedades:

</p>

<p>
  <li>a. Diarrea Viral Bovina: cursa con síntomas leves o directamente subclínica.</li>

  <li>b. Enfermedad de las Mucosas: los PI se sobre infectan con cepas citopáticas,

  desarrollando una enfermedad mortal de curso agudo o crónico.</li>

</p>
<p>

Este virus puede ser aislado de todos los fluidos naturales. El diagnóstico de aborto por

DVB requiere la detección de la infección fetal acompañado de lesiones en el feto, con

cambios patológicos compatibles o antecedentes serológicos del rodeo. La infección por

DVB en el feto no siempre resulta en muerte fetal o aborto, el virus puede persistir en el

feto sin inducir aborto, y ocasionalmente es encontrado en fetos abortados por otras

enfermedades. La detección del virus, no significa que sea la causa de aborto.

Como estrategia de prevención de la enfermedad es la utilización de vacunas. Son a virus

inactivado y debe contener los dos subtipos.
</p>

<h3>b. Herpes Virus Bovino tipo I:</h3>
<p>
  La infección por Herpes Virus Bovino tipo I (HVB‐1) en las vaquillonas puede causar

infertilidad y muerte embrionaria como resultado de infecciones en la preñez temprana.

El virus puede persistir en una infección latente luego de una infección aguda. El aborto

generalmente ocurre en el último tercio de la gestación y el período desde la infección

materna hasta el aborto es variable.

El uso de vacunas como estrategia de prevención es lo más económico y eficiente.
</p>

<h2>Obtención de muestras para diagnóstico del aborto infeccioso bovino</h2>

<p>
  De no ser posible el envío del feto entero (sin extremidades, refrigerado y en condiciones)

a un laboratorio de diagnóstico para ser correctamente muestreado, proceder a tomar las

siguientes muestras:
</p>

<li>a. Muestras de bazo y pulmón (recipientes estériles individuales y refrigerarlas a 4ºC):

cultivo bacteriano y viral.</li>
<li>b. Líquido de cavidad torácica, abdominal o pericárdica: serología de Neosporosis y

enfermedades virales.</li>

<li>c. Líquido abomasal (recipientes estériles individuales y refrigerarlas a 4ºC): cultivo

bacteriano y viral.</li>

<li>d. Improntas de riñón, hígado y pulmón: inmuno-fluorescencia directa (IFD) para

Leptospira sp.</li>

<li>e. Muestras de pulmón, hígado, bazo, riñón, corazón, ganglios linfáticos y en lo posible

cerebro entero. Todas estas en formol al 10%.</li>


<h1 class="title-producir-inside color-sanidad">CAMPYLOBACTERIOSIS GENITAL BOVINA</h1>
<p>
  Es una enfermedad venérea cuyo principal agente responsable es el Campylobacter fetus,

el cual posee dos subespecies conocidas: C. fetus subsp. fetus que habita naturalmente el

intestino y puede ocasionalmente encontrarse en la cavidad prepucial del toro por

contaminación fecal, siendo reconocida últimamente como la más frecuente observada en

fetos; y C. fetus subsp. venerealis que es exclusivamente genital y solo se transmite por vía

venérea. Además, existe el Campylobacter jejuni quien también reside en el tracto

intestinal pudiendo producir abortos esporádicos.
</p>

<h2>Presentación de la enfermedad:</h2>

<p>
  Diremos en general, que la Campylobacteriosis causa infertilidad, muerte embrionaria

temprana y ocasionalmente aborto.

Una característica frecuente que hace sospechar la presencia de la enfermedad es la

disminución del resultado esperado de preñez al momento de realizar el tacto que evalúa

el último servicio. Puede haber o no un cambio en la distribución de la cabeza (disminuida)

y cola (aumentada) de preñez.

La Campylobacteriosis en los toros es crónica, asintomática, sin afectar su libido ni su

fertilidad siendo más frecuente en toros adultos y viejos.

Es posible encontrar toros infectados, sin haber asociación a malos resultados de preñez

en el último servicio. El toro puede haber contraído la enfermedad en un rodeo vecino,

fuera de la temporada.
</p>

<p>
  La infección en la hembra suele ser autolimitante con inflamación uterina media a

moderada, cursando con infertilidad y/o aborto. Pueden detectarse hembras portadoras

crónicas sin que manifiesten signos clínicos, quienes paren normalmente y continúan

infectadas, con carga bacteriana en vagina hasta el siguiente servicio, reinfectando así al

toro y a través de éste al resto del rodeo. Las vaquillonas son susceptibles de infección la

cual puede causar endometritis, mortalidad embrionaria temprana y abortos en la mitad

de la gestación.
</p>

<h2>Prevención:</h2>

<p>
  Como estrategia de prevención de la enfermedad es la utilización de una vacuna de

calidad, que contenga ambas subespecies de la bacteria (C. fetus fetus y C. fetus

venerealis). En primovacunados y toros, aplicar 2 dosis

con intervalos de 21 a 35 días, unos 60 y 30 días antes del servicio o la Inseminación

Artificial. La revacunación anual con una dosis en animales vacunados anteriormente, 30

días antes del servicio o la Inseminación Artificial.

Se debe realizar, además de la vacunación, los controles sanitarios de todos los toros:

raspaje prepucial para diagnóstico por IFD. Se deben obtener al menos 2 raspajes

negativos consecutivos.
</p>

<h2>Diagnóstico:</h2>
<p>
  La muestra obtenida del raspaje prepucial es analizada por inmuno-fluorescencia directa

(IFD), siendo la técnica más ampliamente utilizada para el diagnóstico de la

Campylobacteriosis.
</p>

<h2>Tratamiento:</h2>

<p>
  Los toro que resulten positivos, en cualquier raspaje, deberían ser descartados.

Excepcionalmente, debido al alto valor genético y/o económico de un individuo, bajo

criterio del Médico Veterinario, puede realizarse el tratamiento con oxitetraciclina de

larga acción, pudiendo alcanzar una eficacia del 95% en toros

infectados con Campylobacter fetus. De todas formas, siempre existe el riesgo de adquirir

nuevamente la enfermedad por lo que debería realizarse nuevamente los controles

sanitarios postratamiento.
</p>


<h1 class="title-producir-inside color-sanidad">TRICOMONOSIS BOVINA</h1>

<p>
  La Tricomonosis bovina es una enfermedades de transmisión venérea cuyo agente

etiológico es un protozoario denominado Tritrichomonas foetus (T. foetus). Es un parásito,

caracterizado por poseer tres flagelos anteriores y uno posterior. Se localiza en el prepucio

y pene de los machos y en útero, cérvix y vagina de las hembras.
</p>

<h2>Presentación de la enfermedad:</h2>
<p>
  La característica más evidente que hace sospechar la presencia de la enfermedad es la

disminución del resultado esperado de preñez al momento de realizar el tacto que evalúa

el último servicio. Es frecuente encontrar, asociado a esta última situación, un cambio en

la distribución de preñez, siendo bajo el porcentaje de hembras preñadas de cabeza y

viéndose aumentado el porcentaje preñado de cola. La razón radica en que la enfermedad

genera principalmente pérdidas embrionarias o fetales tempranas, pudiéndose volver a

preñar al final del servicio.

Es posible encontrar toros infectados, sin haber asociación a malos resultados de preñez

en el último servicio. El toro puede haber contraído la enfermedad en un rodeo vecino,

fuera de la temporada.

El macho es portador crónico y responsable de la diseminación de la enfermedad. Está

descripto que las hembras pueden portar el parásito hasta varios meses después de

adquirir la infección.
</p>
<h2>Prevención:</h2>
<p>
  No existen vacunas eficaces para la prevención de ésta enfermedad. La única herramienta

disponible es el saneamiento del rodeo, a través de los controles sanitarios de todos los

toros y el descarte de aquellos que resulten enfermos.

Esquema controles sanitarios: raspaje prepucial para diagnóstico por cultivo. Se deben

obtener al menos 2 raspajes negativos consecutivos.

No existe tratamiento, siendo el descarte el único destino de animales positivos.
</p>

<h2>Diagnóstico:</h2>

<p>
  El diagnóstico de esta enfermedad puede realizarse en machos, hembras y fetos, aunque

el punto de partida en la búsqueda de explicaciones ante una situación inesperada de un

mal resultado de preñez, son los toros.

La muestra obtenida del toro por raspaje prepucial es enviada a cultivo para posterior

observación directa en microscopio. Esta técnica tiene la desventaja de no poder

distinguir Tritrichomonas foetus de otras Tricomonas no patógenas (que tienen un número

diferente de flagelos). Para ello, se desarrolló una técnica de PCR, que permite la

identificación y tipificación del parásito.

En la hembra puede analizarse el mucus cérvico vaginal (MCV) o descargas uterinas y en el

feto puede realizarse el diagnóstico tomando muestras de líquido abomasal o pulmón, o

bien placenta, membranas o líquidos fetales.
</p>

